    static constexpr inline int fastfloor(float fp)
    {
        int i = static_cast<int>(fp);
        return (fp < i) ? (i - 1) : (i);
    }

    static constexpr inline glm::vec3 localCornerToRealCoord(float i, float j, float k, int corner, float step_size)
    {
        switch (corner)
        {
        case 0:
            return { i, j, k };
        case 1:
            return {i + step_size, j, k };
        case 2:
            return { i + step_size, j + step_size, k };
        case 3:
            return { i, j + step_size, k };
        case 4:
            return { i, j, k + step_size };
        case 5:
            return { i + step_size, j, k + step_size };
        case 6:
            return { i + step_size, j + step_size, k + step_size };
        case 7:
            return { i, j + step_size, k + step_size };
        default:
            ENG_LOG_F("Corner index %d is invalid! Must be less than 8.", corner);
            return {};
        }
    }
    
    static bool operator<(glm::vec4 const & left, glm::vec4 const & right)
    {
        if (left.x < right.x)
            return true;
        else if (left.x > right.x)
            return false;

        if (left.y < right.y)
            return true;
        else if (left.y > right.y)
            return false;

        if (left.z < right.z)
            return true;
        else if (left.z > right.z)
            return false;

        return false;
    }

    static inline glm::vec3 interpolateVertex(glm::vec4 p1, glm::vec4 p2, float surface_level)
    {
        if (p2 < p1)
        {
            glm::vec4 temp = p1;
            p1 = p2;
            p2 = temp;
        }
        return (fabs(p1.w - p2.w) > 0.00001) ? glm::vec3(p1) + (surface_level - p1.w) * (glm::vec3(p2) - glm::vec3(p1)) / (p2.w - p1.w) : p1;
    }

    void World::generateWorld(float grid_size, float step_size, float surface_level)
    {
        auto start = std::chrono::high_resolution_clock::now();
        std::vector<float> vertices;

        auto function = [](float x, float y, float z) -> float
        {
            //float const SCALE = 0.6f;
            //return SimplexNoise::noise(x * SCALE, y * SCALE, z * SCALE);
            return x * x + y * y + z * z - 1;
        };

        for (float i = -grid_size; i < grid_size; i += step_size)
        {
            for (float j = -grid_size; j < grid_size; j += step_size)
            {
                for (float k = -grid_size; k < grid_size; k += step_size)
                {
                    // Create index into edgeTable based on values at the vertices of the current cube
                    int cube_configuration = 0;
                    if (function(i, j, k) < surface_level)                                        cube_configuration |= 1;
                    if (function(i + step_size, j, k) < surface_level)                            cube_configuration |= 1 << 1;
                    if (function(i + step_size, j + step_size, k) < surface_level)                cube_configuration |= 1 << 2;
                    if (function(i, j + step_size, k) < surface_level)                            cube_configuration |= 1 << 3;
                    if (function(i, j, k + step_size) < surface_level)                            cube_configuration |= 1 << 4;
                    if (function(i + step_size, j, k + step_size) < surface_level)                cube_configuration |= 1 << 5;
                    if (function(i + step_size, j + step_size, k + step_size) < surface_level)    cube_configuration |= 1 << 6;
                    if (function(i, j + step_size, k + step_size) < surface_level)                cube_configuration |= 1 << 7;

                    // Calculate real vertices at the intersection point based on edgeTable value
                    int edge_configuration = edge_table[cube_configuration];
                    if (edge_configuration == 0) continue;
                    glm::vec3 vertex_list[12];
                    for (int edge = 0; edge < 12; ++edge)
                    {
                        if (!(edge_configuration & 1 << edge)) continue;
                        glm::vec3 cornerA = localCornerToRealCoord(i, j, k, cornerIndexAFromEdge[edge], step_size),
                                  cornerB = localCornerToRealCoord(i, j, k, cornerIndexBFromEdge[edge], step_size);
                        vertex_list[edge] = interpolateVertex(
                            glm::vec4(cornerA, function(cornerA.x, cornerA.y, cornerA.z)),
                            glm::vec4(cornerB, function(cornerB.x, cornerB.y, cornerB.z)),
                            surface_level
                        );
                        //vertex_list[edge] = (cornerA + cornerB) / 2.0f;
                    }

                    // Push back vertices in the right order
                    int const * index_configuration = {tri_table[cube_configuration]};
                    for (int tri_i = 0; index_configuration[tri_i] != -1; tri_i += 3)
                    {
                        glm::vec3 vertex_1 = vertex_list[index_configuration[tri_i]];
                        glm::vec3 vertex_2 = vertex_list[index_configuration[tri_i + 1]];
                        glm::vec3 vertex_3 = vertex_list[index_configuration[tri_i + 2]];

                        // Generate normal
                        glm::vec3 normal = glm::normalize(glm::cross(vertex_2 - vertex_1, vertex_3 - vertex_1));

                        vertices.push_back(vertex_1.x);
                        vertices.push_back(vertex_1.y);
                        vertices.push_back(vertex_1.z);
                        vertices.push_back(normal.x);
                        vertices.push_back(normal.y);
                        vertices.push_back(normal.z);

                        vertices.push_back(vertex_2.x);
                        vertices.push_back(vertex_2.y);
                        vertices.push_back(vertex_2.z);
                        vertices.push_back(normal.x);
                        vertices.push_back(normal.y);
                        vertices.push_back(normal.z);

                        vertices.push_back(vertex_3.x);
                        vertices.push_back(vertex_3.y);
                        vertices.push_back(vertex_3.z);
                        vertices.push_back(normal.x);
                        vertices.push_back(normal.y);
                        vertices.push_back(normal.z);
                    }
                }
            }
        }

        int const VERTEX_SIZE = 6;
        // Generate indices for the vertex list
        std::vector<int> indices(vertices.size() / VERTEX_SIZE);
        for (int i = 0; i < vertices.size() / VERTEX_SIZE; ++i)
        {
            indices.push_back(i);
        }
        if (vertices.size() == 0 || indices.size() == 0) return;
        
        m_vertex_buffer = std::make_shared<VertexBuffer>(&vertices[0], vertices.size() * sizeof(float), VertexBufferLayout{{{3, GL_FLOAT}, {3, GL_FLOAT}}});
        m_vertex_array = std::make_shared<VertexArray>(&indices[0], indices.size() * sizeof(int));
        m_vertex_array->setVertexBuffer(m_vertex_buffer);
        auto duration = std::chrono::high_resolution_clock::now() - start;
        ENG_LOG_F("Generation took %f ms", duration.count() / 1000000.0f);
    }

    void World::render(FirstPersonCamera const & camera) const
    {
        m_shader->bind();
        m_shader->setUniformMatrix4f("u_model", glm::mat4(1.0f));
        m_shader->setUniformMatrix4f("u_view", camera.getViewMatrix());
        m_shader->setUniformMatrix4f("u_projection", camera.getProjectionMatrix());
        m_shader->setUniformVector3f("u_camera_position_W", camera.getPosition());
        m_vertex_array->bind();
        m_vertex_array->drawElements();
    }
